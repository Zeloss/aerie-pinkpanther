<p><span style="text-decoration: underline;">Context</span> :<br />
City of Euhéméria is a City of Magic. The Mayor of Euhéméria offers to adventurers some quests. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- You must meet the Mayor of Euhéméria [ars_fild05,195,193]. It will offer you quests, choose the Lighthouse Traian. The lighthouse doesn't shine on boats for several days, you must reach a certain Garric Frash for investigating on the spot. [ars_fild04] To get to the Lighthouse of Traia area you must head towards the village of Soknobeus [ars_fild03] then take the road south-west. </ p>

<p><center><img src="../images/acces11.2.1.01.jpg" alt="acces11.2.1.01.jpg" /></center></p>

<p>2- Garric frash [ars_fild04,205,149] is located at the entrance of the lighthouse. He will give you the key to enter in the Lighthouse which is full of monsters ... You have to find the lighthouse keeper. [ars_phare,79,196] </ p>

<p><center><img src="../images/acces11.2.2.01.jpg" alt="acces11.2.2.01.jpg" /></center></p>
<p><center><img src="../images/acces11.2.2.02.jpg" alt="acces11.2.2.02.jpg" /></center></p>
