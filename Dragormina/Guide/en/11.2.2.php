<p>3- The crystal used to power the lighthouse light was broken by monsters of the Shadow. You must find the only manufacturer of magic crystals of Arsinoe: Gardulf Grisaire who lives in the Monts Bacchias. [ars_fild05,275,258] While honoring a royal commission doesn't interesting him at all, he gives you the crystal. Go back to the lighthouse keeper to bring him the crystal. </ p>

<p><center><img src="../images/acces11.2.2.04.jpg" alt="acces11.2.2.04.jpg" /></center></p>

<p>4- The Lighthouse Keeper gives you the key to reach the summit but forgets to tell you where to find the entrance! It lies in [ars_phare,226,159}. Once a record, you must remove the Lord of Shadows in order to regain the place! It will be a long and difficult fight </ p>

