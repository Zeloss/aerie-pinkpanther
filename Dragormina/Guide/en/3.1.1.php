<p><h2>3.1.1-The Caves of Triplets</h2></p>

<p>Quest : <em>The Caves of Triplets</em><br />
Prerequisites : 11.2.1- The Bandits Bacchias</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Context</span> :<br />
The Cult of Danifae is highly secretive and few people know it. It's based on the worship of the goddess's children. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- You must meet Julian the Apostate at Ecumenical temple of Euhéméria. [ars_in01,75,142] He said the Danifae's triplets no longer respond to the prayers of the faithful and have even become aggressive! You'll have to find three secret caves and investigate in them. </ p>

<p><center><img src="../images/acces3.1.1.01.jpg" alt="acces3.1.1.01.jpg" /></center></p>

<p>2- You will find a priest of Danifae in the Col of Faymour [ars_fild07,146,99] He talks about the first temple which is located a few meters away. You will find inside this cave Calliope, you must kill her. Once done, take the portal you will receive the Soul of Calliope. Back talk with the Priest of Danifae, he tells you that you must discover the location of other temples. </ p>

<p><center><img src="../images/acces3.1.1.02.jpg" alt="acces3.1.1.02.jpg" /></br>
<img src="../images/acces3.1.1.03.jpg" alt="acces3.1.1.03.jpg" /></center></p>

<p>3- Return to Julian the Apostate, he will explain the situation with the evidence discovered in the cave. The second temple is located in Coppalana [coppa,286,292] and this time you will have to retrieve the soul of Euterpe. </ p>

<p><center><img src="../images/acces3.1.1.04.jpg" alt="acces3.1.1.04.jpg" /></center></p>

<p>4- The third cave is located in the library of Euhéméria. Once inside, you will have to eliminate Urania to retrieve her soul. </ p>

<p><center><img src="../images/acces3.1.1.05.jpg" alt="acces3.1.1.05.jpg" /></center></p>

<p>5- Now that you have three souls, you can relate to Julian the Apostate. You will be rewarded, he will advise you to visit the main temple of Danifae in the capital, Arsinoe.

<p><em>End of the Quest : The Caves of Triplets</em><br />
