<p><span style="text-decoration: underline;">Context</span> :<br />
Access to Arsinoe requires a permit from city authorities. Unfortunately the authorities don't gives them anymore. The chief Pevnec said you that he had a contact who can provide you an authorization. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- Chief Pevnec said you to contact his friend in Euhéméria, Philerme. Back in the forest of Kiman Fares [ars_fild01] then take the north-west road. Here you are in the City of Magic, Euhéméria [ars_fild05] The hostel is located in [ars_fild05,206,193], inside you will find Philerme, give him the letter of Pevnec [ars_in01,220,57]. </ p >

