<p><h2>2.2- The Boitata quest</h2></p>

<p>Quest : <em>Boitata Wrath and the Daggard Bridge</em><br />
Prerequieres : 2.1- In depature to Coppalana</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Context</span> :<br />
You are finally at a small village in the south of Arsinoe : Coppalanna. Guellronoc'h just landed you there. The road looks even longer to reach the capital. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- You must find your guide in Coppalana : Daphnis [coppa,231,124]. He tells you that you must leave the area of Coppalana, you need an authorization from the Chief of the Village, then cross the Daggard's Bridge. The village chief is the Chief Rafloc'h [coppa,235,163], He's on the central square of the village. He tells us that the spirit of the village, Boitata, is angry and destroyed the bridge Daggard, which prevents you from leaving the area. </ p>

<p><center><img src="../images/acces2.2.01.jpg" alt="acces2.2.01.jpg" /><img src="http://www.lunedor.fr/guide/images/acces2.2.02.jpg" alt="acces2.2.02.jpg" /></center></p>

<p>2- You'll have to calm the boitata then the bridge will be able to be rebuilt. Shaman Xaviral [coppa,95,55] tells you how to invoke the boitata. You should just contact Kloiacha via an underpass. [coppa,59,87] </p>

<p><center><img src="../images/acces2.2.03.jpg" alt="acces2.2.03.jpg" /><br />

<img src="../images/acces2.2.04.jpg" alt="acces2.2.04.jpg" /></center></p>

<p>3- The maid Kloiacha invokes the Boitata, you'll have to calm down it with your hammer. Here we go for your first fight in Arsinoe !</ p>

<p><center><img src="../images/acces2.2.05.jpg" alt="acces2.2.05.jpg" /></center></p>

<p>Once the battle is over a message will appear telling you to go back to the Chief of Coppalana. As in most instances of Arsinoe, you got two minutes to leave the room (here the tunnel) to complete the quest. So go and don't lost time !</ p>

<p><center><img src="../images/acces2.2.06.jpg" alt="acces2.2.06.jpg" /></center></p>

<p>4- Chief Rafloc'h commend you for your bravery and tells you that Daggard Bridge is under reconstruction. He offers a new mission: Bring a letter to the tribes of Kiman Fares to tell them that the bridge was again repaired and that their relationship can resume. Another quest will be proposed, Klerozonheg, the cook of the village intends to organize the village fete. </ p>

<p>Head to the northwestern Coppalana to take the Daggard Bridge and enter in the forest of Kiman Fares. </ p>

