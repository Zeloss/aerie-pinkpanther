<p><h2>10.3- Titre de Pêcheur d'Arsinoé</h2></p>

<p>Quête : <em>Titre de Pêcheur d'Arsinoé</em><br />
Pré-requis : Avoir accès à la quasi-totalité des régions d'Arsinoé.</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Arsinoé est riche en poissons d'eau douce et salée. Zeladys un pêcheur de Coppalana vous propose un concours de pêche pour savoir qui est le meilleur pêcheur !</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Zeladys vous demande d'aller acheter une canne à pêche au lac de Crystalmir chez Lyrric. Celui-ci vous proposera une canne à 50 pièces d'argent. Acceptez puis retournez voir Zeladys sur le plage de Coppalana.</p>

<p><center><img src="../images/acces10.3.01.jpg" alt="acces10.3.01.jpg" /></center></p>
<p><center><img src="../images/acces10.3.02.jpg" alt="acces10.3.02.jpg" /></center></p>

<p>2- Il va vous falloir désormais collecter un certain nombre de poissons puis les montrer à Zeladys pour valider votre quête et recevoir le Titre de Pêcheur de l'Arsinoé.</p>
