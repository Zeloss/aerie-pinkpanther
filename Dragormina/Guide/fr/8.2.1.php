<p>2- Vous devez rencontrer Tivias Ranalc'h dans son domaine. Vous devez relever son défi pour devenir mercenaire en réussissant l'épreuve du Champ de Mars.</p>

<p><center><img src="../images/acces8.2.1.03.jpg" alt="acces8.2.1.03.jpg" /></center></p>

