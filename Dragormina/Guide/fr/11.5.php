<p><h2>11.5- Les Pleurs de Tarine</h2></p>

<p>Quête : <em>Les Pleurs de Tarine</em><br />
Pré-requis : 9.1- La Lime du Torn-Thalas</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Guerre de la Porte Nord contre les Gobelins a fait beaucoup de veuves en Arsinoé.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Tarine, habitante d'Euhéméria, est à la recherche de son époux soldat dans la I Légion de l'Arsinoé actuellement engagée dans le conflit avec les Gobelins près de la Lime du Torn-Thalas.</p>

<p><center><img src="../images/acces11.5.01.jpg" alt="acces11.5.01.jpg" /></center></p>

<p>2- Sur la lime en questionnant quelques soldats, vous en apprendrez plus sur le Soldat Goussil, époux de Tarine. Le Caporal Léon le connait et confirme son envoi en mission sous le commandement du Caporal Castille vers l'Oasis de Siwah.</p>

<p><center><img src="../images/acces11.5.02.jpg" alt="acces11.5.02.jpg" /></center></p>

<p>3- Le Caporal de Castille se trouve au Village de Nèsos. Il confirme la mort du Soldat Goussil lors d'une attaque surprise gonbline dans le Désert de Kargah. Retournez voir Tarine pour lui annoncer la nouvelle, elle vous demandera de retrouver sa dépouille pour lui offrir une sépulture décente.</p>

<p><center><img src="../images/acces11.5.03.jpg" alt="acces11.5.03.jpg" /></center></p>

<p>4- Vous retrouverez des lambeaux et une plaque dans le désert de Kargah. (Et peut-être comme moi un autre mort au même endroit ...)</p>

<p><center><img src="../images/acces11.5.04.jpg" alt="acces11.5.04.jpg" /></center></p>

<p>5- Le Frère Jacques au village de Nésos pourra s'occuper de son corps et sa tombe, votre mission est accomplie. Retournez voir Tarine pour obtenir votre prix ...</p>

<p><center><img src="../images/acces11.5.05.jpg" alt="acces11.5.05.jpg" /></center></p>

