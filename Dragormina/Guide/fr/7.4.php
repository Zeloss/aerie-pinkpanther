<p><h2>7.4- L'insurrection de Da'Urdein</h2></p>

<p>Quête : <em>L'insurrection de Da'Urdein</em><br />
Pré-requis : 7.3- Vers la cité d'Ombreterre</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous voilà enfin dans la Cité de Da'Urdein, vous devez demander des comptes aux autorités pour l'attaque du Fort Orage.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous pouvez commencer votre exploration de la Cité de Da'Urdein, le centre du pouvoir se situe devant la Sphère Planaire. En vous adressant au Garde de la Sphère, il vous refusera l'entrée, il vous est donc impossible de vous entretenir avec le Grand Prêtre Rupoldic'h. </p>

<p><center><img src="../images/acces7.4.01.jpg" alt="acces7.4.01.jpg" /></center></p>
<p><center><img src="../images/acces7.4.02.jpg" alt="acces7.4.02.jpg" /></center></p>

<p>2- En vous promenant dans la Cité vous croiserez Iravie Cofic. Selon lui le Grand Prêtre agit étrangement depuis des semaines et a mené l'opération contre l'Hylarheim de son propre chef. Il vous a demandé de retrouvé une liche dans la forêt rouge pour connaitre un peu mieux la situation. </p>

<p><center><img src="../images/acces7.4.03.jpg" alt="acces7.4.03.jpg" /></center></p>

<p>3- En vous promenant dans la forêt rouge vous rencontrerez Svankmajer, la liche en question. Cette liche est un rénégat, banni par le Grand Prêtre pour des raisons politiques. Il tente de monter une insurrection à Da'Urdein pour mettre fin à la main mise du pouvoir par le Grand Prêtre. Il vous recommande de rencontrer Rybzinsky dans les soupirs du Morloc'h, l'actuel chef militaire de la résistance.</p>

<p><center><img src="../images/acces7.4.04.jpg" alt="acces7.4.04.jpg" /></center></p>

<p>4- Rybzinsky a préparé des troupes dans les Soupirs du Morloc'h, bien que peu nombreux, ils ne devraient pas avoir de problèmes pour prendre le contrôle de la ville tant les soutiens au Grand Prêtre sont peu nombreux.</p>

<p><center><img src="../images/acces7.4.05.jpg" alt="acces7.4.05.jpg" /></center></p>

<p>5- Vous devez rechercher un passage secret dans une maison de Da'Urdein puis l'emprunter pour rejoindre les résistants. Iravie Corfic vous mènera sur le champ de bataille.</p>

<p><center><img src="../images/acces7.4.06.jpg" alt="acces7.4.06.jpg" /></center></p>
<p><center><img src="../images/acces7.4.07.jpg" alt="acces7.4.07.jpg" /></center></p>

<p>6- Votre objectif est de faire chuter les Brigadiers de Da'Urdein pour que les troupes arrêtent le combat. Vous en avez quatre à tuer.</p>

<p><center><img src="../images/acces7.4.08.jpg" alt="acces7.4.08.jpg" /></center></p>

<p>7- Maintenant personne ne garde la Sphère, il est temps de s'y rendre pour avoir une discussion avec le Grad Prêtre.</p>

