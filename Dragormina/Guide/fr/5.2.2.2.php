<p><h2>5.2.2.2- Le Corps de Djoser</h2></p>

<p>Quête : <em>Le Corps de Djoser</em><br />
Pré-requis : 5.2.2.1- A la recherche des larves de Kargah</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Pour pouvoir accèder aux larves de Kargah ainsi qu'au Portail des Abysses, vous allez devoir lever les runes protectrices du Temple de Pétosiris.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- En entrant dans le temple vous tomberez face à une momie dans le mur : Le Gardien Lesmonas. Il est le gardien de la tombe de Djoser, mais celle-ci est vide, cet antique mage est le seul à pouvoir annuler les protections magiques du temple. Son corps a été découpé et caché dans différentes parties de l'Arsinoé, il va vous falloir les réunir pour avancer dans les entrailles du temple.</p></p>

<p><center><img src="../images/acces5.2.2.2.01.jpg" alt="acces5.2.2.2.01.jpg" /></center></p>

<p>2- La première crypte se situe dans les Monts-Bacchias au nord du lac de Crystalmir dans la région des Tours Violettes [ars_fild51]. Vous devez tuer le Protecteur de la tombe pour récupérer la tête de Djoser à la sortie.</p>

<p><center><img src="../images/acces5.2.2.2.02.jpg" alt="acces5.2.2.2.02.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.2.03.jpg" alt="acces5.2.2.2.03.jpg" /></center></p>

<p>3- La deuxième crypte se situe dans l'Hylarheim, au nord de cette ville dans les plaines de Daltigoth [ars_fild50]. Renouvellez l'opération pour obtenir le corps de Djoser.</p>

<p><center><img src="../images/acces5.2.2.2.04.jpg" alt="acces5.2.2.2.04.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.2.05.jpg" alt="acces5.2.2.2.05.jpg" /></center></p>

<p>4- La dernière crypte est camouflée dans la Jungle de Thébaïde, dans la région de la Tour des Etoiles [ars_fild17]. Vous obtiendez cette fois-ci les jambes de Djoser.</p>

<p><center><img src="../images/acces5.2.2.2.06.jpg" alt="acces5.2.2.2.06.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.2.07.jpg" alt="acces5.2.2.2.07.jpg" /></center></p>

<p>5- De retour au Temple de Pétosiris, approchez-vous du sarcophage de Djoser pour y déposer les parties du corps de celui-ci. Il voudra bien entendu vous tuer pour se venger de son enfermement, il vous faudra l'éliminer pour lever la première Rune puis poursuivez votre exploration du temple.</p>

