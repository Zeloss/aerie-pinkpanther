<p><h2>I- Introduction</h2></p>

<p>Quête : <em>Urunesti, La Vision</em> (facultative)<br />
Pré-requis : Aucun</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :</p>

<p>Tout commence quand vous créez un personnage. Il va naître dans une ville d’Arsinoé appelée Urunesti, qui est votre ville natale. Avancez vers la position [54,77]. Il vous sera demandé de choisir une langue entre le Français, l’Anglais et l’Espagnol. Une fois que vous aurez choisi votre langue, vous verrez un message de ‘’maman ‘’ vous disant de rentrer à la maison !</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Ceci est votre première quête : Urunesti, la vision (pour consulter votre journal de quêtes faites alt+u. Vous n’aurez besoin d’aucun objet/équipement pour cette quête). Alors dirigez-vous vers la maison en [98,100] et entrez-y. Une fois à l’intérieur, allez en [22,30]. Vous verrez le npc ‘’Maman’’ avec au dessus de sa tête un message ‘’quête ! ‘’. Elle vous demandera d’aller chercher du lait à la ferme, chez un certain ‘’Fernandel’’ qui se trouve au nord d’Urunesti.</p>

<p><center><img src="http://www.lunedor.fr/guide/images/intro01.jpg" alt="intro01.jpg" /></center></p>

<p>2- Sortez de la maison et prenez le pont de la ville pour entrer dans le warp. Ensuite, allez en [136,75] et vous verrez Fernandel. Parlez-lui, et ensuite parlez à la vache à côté de lui pour la traire. Ceci peut ne pas marcher du premier coup, et vous recevez une attaque qui vous enlève 10% de vos points de vie.</p>

<p><center><img src="http://www.lunedor.fr/guide/images/intro02.jpg" alt="intro02.jpg" /></center></p>

<p>3- Une fois la bouteille de lait en poche, vous devez revenir dans votre maison. Celle-ci vous dit de monter vous reposer. A l'étage, vous croiserez votre sœur Anne en [45,101], puis près du lit vous verrez un NPC action [action : dormir]. Il est temps de piquer un somme !</p>

<p><center><img src="http://www.lunedor.fr/guide/images/intro03.jpg" alt="intro03.jpg" /></center></p>

<p>4- Il vous sera demandé si vous voulez vous reposer. Répondez oui. Une fois cela fait, vous serez téléporté dans un endroit étrange. Avancez en suivant le chemin, puis parlez au mage qui se trouve juste devant. [63,99] Parlez-lui, une cinématique se déclenchera automatiquement. Vous vous trouvez, a la fin de cette cinématique, dans un autre endroit. Vous avez été sauvé par l’Archimage Ornélia et ses trois mages. Avancez en [133,87] et une autre cinématique se déclenchera.<br />
<center><br />
<img src="http://www.lunedor.fr/guide/images/intro04.jpg" alt="intro04.jpg" /><br />
<img src="http://www.lunedor.fr/guide/images/intro05.jpg" alt="intro05.jpg" /></center></p>

<p>Une fois la cinématique terminée, un portail s’ouvrira. Entrez-y. Vous serez de retour à Urunesti, dans votre maison.</p>

<p>5- Sortez de votre maison. Rendez-vous dans la demeure de Franshille en [130,73] puis parlez-lui. Il vous demandera si vous êtes d’accord pour tout quitter et d’accomplir votre destin. Répondez oui ! Vous serez alors téléporté dans un tunnel magique. Suivez le chemin et entrez dans le portail juste devant vous. Dès lors vous vous retrouverez à la Capitale, ce n’est que le début d’une grande aventure, que vous reprendrez une fois plus fort…</p>

<p><center><img src="http://www.lunedor.fr/guide/images/intro06.jpg" alt="intro06.jpg" /></center></p>

