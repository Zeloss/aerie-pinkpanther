<p><h2>6.2- Le Titre de Gladiateur</h2></p>

<p>Quête : <em>Le Titre de Gladiateur</em><br />
Pré-requis : 6.1.3- Les Arènes de La Rana</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Les Habitants de La Rana adorent les combats dans leur Arène. Ils proposent différents types de Combat aux gladiateurs. Pour obtenir le Titre de Gladiateur de La Rana vous devez obtenir 200 points au près du Maitre Shugo.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Retournez voir le Maitre Shugo, vous avez plusieurs épreuves à réaliser pour remporter des Points.</p>

<p><center><img src="../images/acces6.2.01.jpg" alt="acces6.2.01.jpg" /></center></p>

<p>2- Vous pouvez lancer le Combat MvP, il consiste en l'affrontement en équipes de MvP. Vous gagnez 5 points pour chaque combat.</p>

<p>3- Vous pouvez choisir le mode Assaut, vous devez résister à plusieurs vagues d'Assaut. Vous gagnez 5 points pour chaque combat.</p>

<p>4- Vous pouvez aussi battre la Déesse de La Rana à nouveau et en équipe. Vous gagnez 5 points pour chaque combat.</p>

