<p><h2>4.1.1- L'Apprenti de la Mage Gawëlle</h2></p>

<p>Quête : <em>L'Apprenti de la Mage Gawëlle</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Magie en Arsinoé est organisée autour du Haut-Conseil de la Magie présidé par l'Archimage Ornélia. Depuis quelques temps des Mages Gris, pratiquant une magie interdite en Arsinoé sont apparus.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- En vous promenant dans la forêt de Kiman Farès au niveau du village de Soknobeus [ars_fild03] vous rencontrez un Poulet. Il vous dit être un humain et qu'il se retrouve bloqué sous cette forme. Il vous implore de retrouver sa maîtresse la Mage Gawëlle dans les Monts Bacchias.</p>

<p><center><img src="../images/acces4.1.1.01.jpg" alt="acces4.1.1.01.jpg" /></center></p>

<p>2- La Mage Gawëlle est dans un petit village près de l'Abbaye du Col du Feymour. [ars_fild07,167,44] Elle reconnait en effet avoir un apprenti peu doué pour la magie .. Elle vous demande de retourner le chercher pour quelle puisse inverser le sortilège.</p>

<p><center><img src="../images/acces4.1.1.02.jpg" alt="acces4.1.1.02.jpg" /></center></p>

<p>3- Une fois finissule dans votre sac, retournez voir la Mage Gawëlle. Vous apprenez que celui-ci a été transformé en poulet par des Mages Gris qui se réunissent la nuit dans la forêt de Kiman Farès. La Mage Gawëlle vous charge d'enquêter à la place de Finissule et de lui rapporter vos observations.</p>

<p>4- Il faudra vous rendre de nuit (20h00-8h00) dans la forêt de Kiman Farès, la partie où se situe le village du même nom [ars_fild02]. En parlant avec ces mages gris vous serez transformé en poule et téléporté vous aussi dans la région de Soknobeus ... Il va vous falloir retourner voir la Mage Gawëlle par vos propres moyens sans pouvoir vous battre !</p>

<p><center><img src="../images/acces4.1.1.03.jpg" alt="acces4.1.1.03.jpg" /></center></p>
<p><center><img src="../images/acces4.1.1.04.jpg" alt="acces4.1.1.04.jpg" /></center></p>

<p>5- La Mage Gawëlle vous donnera une lettre de recommandation pour pouvoir entrer au Haut-Conseil de la Magie et y faire part de vos observations.</>

