<p><h2>10.1- Titre de Magnat d'Arsinoé</h2></p>

<p>Quête : <em>Titre de Magnat d'Arsinoé</em><br />
Pré-requis : 2.4- Les faux-papiers pour Arsinoé </p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Compagnie Umaya contrôle tout le commerce de l'Arsinoé. Vous pouvez travailler pour cette compagnie est gravir les échelons pour obtenir le Titre de Magnat d'Arsinoé ainsi que des prix avantageux.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez vous rendre tout d'abord visite aux locaux de la Compagnie d'Umaya à Euhéméria. Parlez avec la responsable, elle vous proposera de devenir membre de la Compagnie pour 50.000 zenys.</p>

<p><center><img src="../images/acces10.1.01.jpg" alt="acces10.1.01.jpg" /></center></p>

<p>2- Rendez-vous à Coppalana pour discuter avec le Rabatteur Harrey, il vous proposera d'ouvrir un établissement dans l'île de Tantale pour faciliter le commercer avec la Thébaïde.</p>

<p><center><img src="../images/acces10.1.02.jpg" alt="acces10.1.02.jpg" /></center></p>

<p>3- Le Chef Ocrah est d'accord sur le principe, vous pouvez retourner voir le Rabatteur Harrey pour lui annoncer cette bonne nouvelle.</p>

<p><center><img src="../images/acces10.1.03.jpg" alt="acces10.1.03.jpg" /></center></p>

<p>4- Représentant Olcha de Kiman Farès a besoin de votre aide. Suite aux attaques des morts-vivants, les vivres se font rares dans le village. Plus rien n'arrive d'Euhéméria pour le moment, mais elle pense pouvoir ouvrir une route commerciale depuis le Phare de Traïana [ars_fild04].</p>

<p><center><img src="../images/acces10.1.04.jpg" alt="acces10.1.04.jpg" /></center></p>

<p>5- Oswald Rijin se trouve au port, il conforme l'arrivée des marchandises et que leur envoi est en cours. Retournez voir Olcha à Kiman Farès pour lui annoncer la bonne nouvelle.</p>

<p><center><img src="../images/acces10.1.05.jpg" alt="acces10.1.05.jpg" /></center></p>

<p>6- Il parait qu'une mission est en préparation pour relier Crystalmir à la Thébaïde. Dans le port de Thébaïde vous devez rencontrer la marchande Jonas, elle souhaite importer des tanches du lac de Crystalmir.</p>

<p><center><img src="../images/acces10.1.06.jpg" alt="acces10.1.06.jpg" /></center></p>

<p>7- Ghywald Rumsdan accepte d'ouvrir une route commerciale. Les tanches seront acheminées par le train depuis Crystalmir. Retournez voir la Marchande Jonas pour valider la quête. Un message est arrivé d'Euhéméria, la responsable Umaya souhaite s'entretenir avec vous.</p>

<p><center><img src="../images/acces10.1.07.jpg" alt="acces10.1.07.jpg" /></center></p>

<p>8- La responsable vous parlera des bandits qui sévissent dans les Monts Bacchias, elle souhaite que vous participiez avec l'aide des de la garnison de la Passe d'Octavie a une mission punitive contre le chef des bandits. Le Lieutenant Van Gothe vous attend dans le fort de la Passe d'Octavie.</p>

<p><center><img src="../images/acces10.1.08.jpg" alt="acces10.1.08.jpg" /></center></p>

<p>9- Les Chevaliers d'Arsinoé ont isolé la base secrète des bandits. Vous devez vous y rendre et vous occuper du seigneur des bandits. Retournez voir Van Goethe.</p>

<p><center><img src="../images/acces10.1.09.jpg" alt="acces10.1.09.jpg" /></center></p>
<p><center><img src="../images/acces10.1.10.jpg" alt="acces10.1.10.jpg" /></center></p>

<p>10- En retournant à Euhéméria, la Compagnie Marchande d'Umaya vous convie dans leurs bureaux d'Arsinoé. Vous devez rencontrer le Directeur Oljicka. Il vous demandera de lui rapporter différents objets. Une fois rapportés, vous recevrez le Titre de Magnat d'Arsinoé.</p>

