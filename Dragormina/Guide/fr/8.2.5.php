<p><h2>8.2.5- La Fronde</h2></p>

<p>Quête : <em>La Fronde</em><br />
Pré-requis : 8.2.4- Le Nid de l'aigle</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous êtes désormais un mercenaire de Néraka, vous allez devoir faire votre réputation en remplissant des missions diverses et variées au Hall des Mercenaires.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Pour chaque début de mission vous devez lire les annonces au panneau des missions. Votre quatrième mission est celle de la Fronde, vous devez rejoindre Tivias devant la Porte de Néraka pour aider à la défense de la Cité attaquée par les forces des Mages Gris et du Comte de Nemours.</p>

<p><center><img src="../images/acces8.2.2.01.jpg" alt="acces8.2.2.01.jpg" /></center></p>

<p>2- Rendez-vous devant la porte de Néraka pour lancer l'instance "La Fronde" en parlant avec Tivias Ranalc'h.</p>

<p><center><img src="../images/acces8.2.5.01.jpg" alt="acces8.2.5.01.jpg" /></center></p>

<p>3- Néraka est envahie par ses ennemis, vous devez tout d'abord vous occuper du chef de guerre de Nemours : Le Colonel Kadafik</p>

<p><center><img src="../images/acces8.2.5.02.jpg" alt="acces8.2.5.02.jpg" /></center></p>

<p>4- Ensuite vous pouvez poursuivre vers la salle du trône. Le Roi Baudouin est protégé par un sort de défense, vous devez le protéger du Comte de Nemours.</p>

<p><center><img src="../images/acces8.2.5.03.jpg" alt="acces8.2.5.03.jpg" /></center></p>

<p>5- Vous apprendrez que l'attaque a été menée depuis les mines de Néraka qui sont reliées aux anciennes mines situées non loin de la Porte ... Vous allez devoir enquêter ... Retournez tout d'abord au Tableau des Missions.</p>

