<p><h2>8.2.4- Le Nid de l'aigle</h2></p>

<p>Quête : <em>Le Nid de l'aigle</em><br />
Pré-requis : 8.2.3- La Livraison de Glace</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous êtes désormais un mercenaire de Néraka, vous allez devoir faire votre réputation en remplissant des missions diverses et variées au Hall des Mercenaires.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Pour chaque début de mission vous devez lire les annonces au panneau des missions. Votre troisième mission est celle du Nid de l'Aigle, vous devez vous rendre dans la demeure du Comte de Nemours qui ne répond plus aux appels de son Roi qui craint une traîtrise de sa part.</p>

<p><center><img src="../images/acces8.2.2.01.jpg" alt="acces8.2.2.01.jpg" /></center></p>

<p>2- Votre mandat de perquisition en poche dirigez-vous vers le Nord-Est du Val de Bise [ars_fild53] et entrez dans la demeure du Comte de Nemours.</p>

<p><center><img src="../images/acces8.2.4.01.jpg" alt="acces8.2.4.01.jpg" /></center></p>
<p><center><img src="../images/acces8.2.4.02.jpg" alt="acces8.2.4.02.jpg" /></center></p>

<p>3- Commencez tout d'abord par vider la première salle pour ouvrir le grand portail.</p>

<p><center><img src="../images/acces8.2.4.03.jpg" alt="acces8.2.4.03.jpg" /></center></p>

<p>4- Une deuxième salle vous attend, en battant les gardes et Mages Gris défendant le Comte de Nemours, celui-ci reconnaitra sa défaite et vous donnera son livre de compte. Il doit être jugé par le Roi Baudouin, retournez dans la salle d'audience royale pour assister à l'interrogatoire.</p>

<p><center><img src="../images/acces8.2.4.04.jpg" alt="acces8.2.4.04.jpg" /></center></p>
<p><center><img src="../images/acces8.2.4.05.jpg" alt="acces8.2.4.05.jpg" /></center></p>

<p>5- Le Roi et ses principaux conseillers sont réunis dans la salle pour juger le Comte de Nemours. Celui-ci est devenu un Noble-Mage avec l'aide de Shandalar, ses comptes montrent qu'il entretient une forte milice sur de l'argent détourné à la couronne. Le Comte se téléporte au loin en annonçant l'imminente attaque sur Néraka par ses troupes !</p>

<p><center><img src="../images/acces8.2.4.06.jpg" alt="acces8.2.4.06.jpg" /></center></p>

<p>6- Retournez voir Tivias Ranalc'h dans le Hall des Mercenaires pour lui annoncer le danger qui pèse sur Néraka. Il acceptera d'aider la couronne, retournez voir le Tableau des Missions.</p>

