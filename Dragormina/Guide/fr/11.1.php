<p><h2>11.1- La Compagnie Ferroviaire</h2></p>

<p>Quête : <em>La Compagnie Ferroviaire</em><br />
Pré-requis : 2.2- La quête de Boitata puis 2.4- Les faux-papiers pour Arsinoé, 6.1.3- Les Arènes de La Rana pour débloquer La Rana.</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
L'Arsinoé est desservi par une Compagnie Ferroviaire qui relie les grandes villes du continent. La ligne principale relie la Thébaïde à La Rana en passant par Arsinoé. Il est utile d'obtenir cet accès qui vous offre un moyen de transport rapide sur le continent.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Le Chef de Gare de la Porte Nord d'Arsinoé vous explique qu'il est interdit de prendre le train si vous n'êtes pas autorisé par les autorités. Mais vu qu'il vous trouve une bonne tête, il va vous proposer un petit marché ...</p>

<p><center><img src="../images/acces11.1.01.jpg" alt="acces11.1.01.jpg" /></center></p>

<p>2- Vous devez rendre visite au Chef de Gare de la Ferme Eupator, vous devez lui apporter 40 lotus noir. Une fois apportés, il vous demandera de régler une de ses dettes au près du chef de gare de la Thébaïde.</p>

<p><center><img src="../images/acces11.1.02.jpg" alt="acces11.1.02.jpg" /></center></p>

<p>3- Vous devez lui apporter 10 lotus noir ... Une fois fait pour pouvez retourner voir le Chef de Gare de la Porte Nord d'Arsinoé. Vous aurez enfin l'autorisation de Voyager sur la ligne Est. Soit de la Thébaïde à la Porte Nord d'Arsinoé.</p>

<p><center><img src="../images/acces11.1.03.jpg" alt="acces11.1.03.jpg" /></center></p>

<p>4- Vous devez parler au Chef de Gare de la Forêt Rouge pour débloquer la destination.</p>

<p><center><img src="../images/acces11.1.04.jpg" alt="acces11.1.04.jpg" /></center></p>

<p>5- Ensuite vous devez rendre visite au Chef de Gare du Cratère de Gondar. Il souhaite que vous preniez des nouvelles d'un ami à lui qui est coincé dans le village de Nésos.</p>

<p><center><img src="../images/acces11.1.05.jpg" alt="acces11.1.05.jpg" /></center></p>

<p>6- Hathare se porte bien vous pouvez rapporter cette bonne nouvelle au Chef de Gare pour débloquer l'accès à Gondar !</p>

<p><center><img src="../images/acces11.1.06.jpg" alt="acces11.1.06.jpg" /></center></p>

