<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Nord d'Arsinoé est un vaste désert, le désert de Kargah. Depuis des siècles des colons de l'Arsinoé ont pris les terres des gobelins, les repoussant jusque dans les régions les plus désolées. Malheureusement, ils ont mené une grande attaque sur Arsinoé, vous venez de les repousser du Fleuve Torn-Thalas, vous allez devoir poursuivre le combat vers l'Oasis de Siwah. </p>

