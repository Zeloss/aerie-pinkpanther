<p><h2>11.6- Les Zombies</h2></p>

<p>Quête : <em>Les Zombies</em><br />
Pré-requis : 2.4- Les faux-papiers pour Arsinoé</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Culte de Mortys est un culte très particulier originaire de la Cité d'Arsinoé. Ils vénèrent les morts et se chargent de leur embaumement. Mais voilà que les morts se lèvent, de peur de se faire interdire dans la Cité, ils recherchent des aventuriers pour rétablir la situation sans alerter les autorités de la ville.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Yummy vous explique la situation de crise que traverse en ce moment son ordre. Elle vous demande de retrouver Tasty dans la ville pour vous recruter.</p>

<p><center><img src="../images/acces11.6.01.jpg" alt="acces11.6.01.jpg" /></center></p>

<p>2- Tasty vous autorise à pénétrer dans le Temple de Mortys ... Il est temps de s'occuper de quelques zombies !</p>

<p><center><img src="../images/acces11.6.02.jpg" alt="acces11.6.02.jpg" /></center></p>

<p>3- Vous allez devoir tenir un certain temps en créant des barricades pour retenir les zombies derrière les barricades. Vous pouvez les reconstruire à tout moment (si vous avez assez de points) pour colmater les failles.</p>

<p><center><img src="../images/acces11.6.03.jpg" alt="acces11.6.03.jpg" /></center></p>
<p><center><img src="../images/acces11.6.04.jpg" alt="acces11.6.04.jpg" /></center></p>

<p>4- Retournez voir Yommy, elle vous apprendra qu'il lui fait désormais un Livre de Sort pour ramener au calme les morts ... Vous devez retrouver Choubi Yama à Arsinoé ... Il vous apprendra que sa maison est envahie par des zombies qui ne le laissent pas entrer ...</p>

<p><center><img src="../images/acces11.6.05.jpg" alt="acces11.6.05.jpg" /></center></p>

<p>5- Vous allez devoir remplir plusieurs missions en un temps limité pour atteindre enfin le livre ... et sa demeure et bien grande !</p>

<p><center><img src="../images/acces11.6.06.jpg" alt="acces11.6.06.jpg" /></center></p>

<p>6- Une fois le livre récupéré vous pourrez le rapporter à Yommy pour qu'elle puisse enfin rendre aux morts leur repos.</p>

<p><em>Fin de la quête : Les Zombies</em><br />
