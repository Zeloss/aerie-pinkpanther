<p><h2>6.1.2- Dian Bian Fût</h2></p>

<p>Quête : <em>Dian Bian Fût</em><br />
Pré-requis : 6.1.1- Les Colons de La Rana</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Rana est une région située au Sud d'Arsinoé. Cette région est une grande forêt où vivent les La Rana. Ils étaient autrefois indépendants mais le Roi d'Arsinoé vient de les soumettre il y a de cela quelques années.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous voilà enfin face au Fort de Dian Bian Fût où est retranché la IV Légion. Les Ranas viennent de faire un barrage faisant augmenter le niveau de l'eau autour du fort rendant une extraction impossible. Vous devez lancer l'instance.</p>

<p><center><img src="../images/acces6.1.2.01.jpg" alt="acces6.1.2.01.jpg" /></center></p>

<p>2- Vous devez lever le siège, foncez tout d'abord dans le camp ennemi pour les bouter. vous pourrez ensuite poursuivre votre route vers le barrage.</p>

<p><center><img src="../images/acces6.1.2.02.jpg" alt="acces6.1.2.02.jpg" /></center></p>

<p>3- La poudre est déjà là, vous devez la faire sauter pour libérer l'eau. De nouvelles troupes arrivent rapidement vous devez vous réfugier dans la Forteresse en prenant l'ascenseur !</p>

<p><center><img src="../images/acces6.1.2.03.jpg" alt="acces6.1.2.03.jpg" /></center></p>

<p>4- Comme vous pouvez le constater, le niveau de l'eau a bien baissé vous allez pouvoir continuer votre route vers la Capitale La Rana. Vous devez prendre l'ascenseur puis discuter avec le Batelier Erih.</p>

