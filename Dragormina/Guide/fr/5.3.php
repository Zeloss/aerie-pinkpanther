<p><h2>5.3- Dynahéir et la Forteresse Gnoll</h2></p>

<p>Quête : <em>Dynahéir et la Forteresse Gnoll</em><br />
Pré-requis : 5.2.2.3- Les Entrailles du Temple / 5.1- Retour en Urunesti</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Il a été décidé par les résistants d'Urunesti de réunir les dernières forces présentes en Arsinoé pour mener la lutte dans les Abysses afin d'empêcher Fenrir de fouler les mondes de l'Yggdrasil.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Retournez dans la demeure de Joka l'Eclairé pour discuter avec l'Archimage Ornélia et lui indiquer votre réussite dans votre mission avec Ursula Hapoi. Il manque encore une personne à l'alliance pour pouvoir ouvrir le portail des Abysses. Il faut absolument un mage maitrisant la magie entropique, magie qui n'est pas la bienvenue dans le Haut-Conseil de la Magie. Ornélia vous demandera d'aller retrouver Dynahéir qui selon elle est une vraie calamité. Il est temps de retourner dans la région de Gondar pour rechercher cette fameuse magicienne.</p>

<p>2- Depuis la Forêt Rouge prenez la direction du Canyon de Gondar, la même route menant vers Lalibela mais cette fois-ci en continuant vers le nord et vous serez à la Forteresse Gnoll [ars_fild56].</p>

<p><center><img src="../images/acces5.3.01.jpg" alt="acces5.3.01.jpg" /></center></p>

<p>3- Vous allez devoir lancer un assaut sur la Forteresse Gnoll pour cela lancez l'instance. Vous devez éliminer Rob et Roy, les deux subalternes du chef du fort, pour accèder à Mc Kenzie. Une fois éliminé, dirigez-vous vers les geôles pour libérer Dynaheïr. Elle invoquera un portail pour vous sortir de là.</p>

