<p><h2>5.1- Retour en Urunesti</h2></p>

<p>Quête : <em>Retour en Urunesti</em><br />
Pré-requis : 4.2 - La Citadelle de la Lumière</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Il est décidé que le combat continuera depuis Urunesti, votre ville natale. Il est encore possible d'empêcher le Ragnarök mais pour cela il va falloir préparer une descente dans les Abysses pour y détruire Fenrir qui rassemble ses forces en vu de détruire les mondes de l'Yggdrasil.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez rencontrer Joka l'éclairé qui héberge les résistants à Urunesti. Il vous mènera dans la salle de réunion pour choisir le meilleur moyen d'arrêter le Ragnarök.</p>

<p><center><img src="../images/acces5.1.01.jpg" alt="acces5.1.01.jpg" /></center></p>
<p><center><img src="../images/acces5.1.02.jpg" alt="acces5.1.02.jpg" /></center></p>

<p>2- Le Portail des Abysses à la Citadelle de la Lumière était temporaire. Le Prophète Tzangue vous parle d'un Portail dans le désert de Kargah mais dont la route est difficile, il faut en effet traverser le Temple de Pétosiris qui est toujours scellé par une magie ancienne. Une exploratrice pourrait vous aider, Ursula Hapoi, hélas elle est très bornée et n'acceptera pas de vous aider tant qu'elle n'aura pas sauvé Urunesti de l'épidémie de Lycantropie qui y sévit.</p>

<p><center><img src="../images/acces5.1.03.jpg" alt="acces5.1.03.jpg" /></center></p>

<p>3- Si vous n'avez toujours pas reçu le Titre de Médecin de l'Arsinoé vous devez vous adressez à Spiria Symphonia qui recherche à Arsinoé des mercenaires pour lever l'épidémie qui sème la mort en Urunesti.</p>

