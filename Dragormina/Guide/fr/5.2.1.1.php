<p><h2>5.2.1.1- En route vers Urunesti</h2></p>

<p>Quête : <em>En route vers Urunesti</em><br />
Pré-requis : 7.3- Vers la Cité d'Ombreterre</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Votre ville natale d'Urunesti est en proie à une étrange maladie qui transforme ses habitants en loup-garou. Spiria Symphonia, une de vos amie d'enfance, est venue à Arsinoé pour rechercher un remède et des personnes de bonne volonté pour l'aider.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Spiria Symphonia [ars_fild22,247,266] se situe dans les jardins d'Arsinoé. Elle vous demande d'aller chercher un livre sur la Peste Rouge à la bibliothèque d'Euhéméria.</p>

<p><center><img src="../images/acces5.2.1.1.01.jpg" alt="acces5.2.1.1.01.jpg" /></center></p>

<p>2- Le Bibliothécaire vous donne un livre sur les épidémies de l'Arsinoé mais celui-ci est écrit en langue savante que seul les médecins peuvent maitriser. Retournez voir Spiria, elle vous remercie et vous demande de rejoindre en Hylarheim, l'Hôpital du Bras Secoureur près du Port-Sirène.</p>

<p><center><img src="../images/acces5.2.1.1.02.jpg" alt="acces5.2.1.1.02.jpg" /></center></p>

<p>3- Pour pouvoir vous en Urunesti, vous devez premièrement atteindre l'Hylarheim mais il vous faut auparavant passer par l'OmbreTerre. Pour connaitre le chemin jusqu'en Hylarheim, reportez vous à ce guide : <a href="7.3.php">Vers la cité d'Ombreterre</a>.</p>

<p>4- Une fois que vous êtes dans le Port-Sirène prenez la route de l'Ouest pour aboutir dans la région de l'Hôpital Secoureur [ars_fild45]. Entrez dans l'Hôpital pour apporter le livre.

<p><center><img src="../images/acces5.2.1.1.03.jpg" alt="acces5.2.1.1.03.jpg" /></center></p>

<p>5- Le Docteur Dumont d'Urville, directeur de l'institut, organise la recherche des ingrédients pour le remède. Il requiert votre aide pour aider ses infirmiers à collecter le tout.</p>

<p><center><img src="../images/acces5.2.1.1.04.jpg" alt="acces5.2.1.1.04.jpg" /></center></p>

