<p><h2>4.2- La Citadelle de la Lumière</h2></p>

<p>Quête : <em>La Citadelle de la Lumière</em><br />
Pré-requis : 4.1.3- Réunion des sages</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
L'Archimage Ornélia concentre désormais le front de résistance face aux ténèbres qui s'agitent en Arsinoé. Shandalar veut ouvrir un portail vers les Abysses pour déclencher le Ragnarök !</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez empêcher l'incantation de Shandalar à tout prix ! L'Archimage Ornélia va vous proposer de vous téléporter directement à la Citadelle de la Lumière.</p>

<p><center><img src="../images/acces4.2.01.jpg" alt="acces4.2.01.jpg" /></center></p>

<p>2- Lancez l'instance : "Incantation de Shandalar". Vous devez premièrement tuer les cinq sombres-acolytes qui bloquent l'entrée de la Citadelle.</p>

<p><center><img src="../images/acces4.2.02.jpg" alt="acces4.2.02.jpg" /></center></p>

<p>3- Une fois l'entrée dégagée, il est temps de se battre contre Shandalar ! Un combat rude vous attend mais le sort du monde est entre vos mains !</p>

<p><center><img src="../images/acces4.2.03.jpg" alt="acces4.2.03.jpg" /></center></p>

<p>4- En tuant Shandalar la liche, vous obtenez le titre d'Héros d'Arsinoé ! Vous vous retrouvez dans l'antichambre des Abysses ... Vous n'avez pas pu empêcher le retour de Fenrir dans notre monde ... Le Ragnaök est désormais inéluctable. La Déesse Aérie apparait est vous donne des explications supplémentaires ... Il est décidé que le combat continuera depuis Urunesti, votre ville natale. Il est encore possible d'empêcher le Ragnarök mais pour cela il va falloir préparer une descente dans les Abysses pour y détruire Fenrir qui rassemble ses forces en vu de détruire les mondes de l'Yggdrasil.</p>

<p><center><img src="../images/acces4.2.04.jpg" alt="acces4.2.04.jpg" /></center></p>

