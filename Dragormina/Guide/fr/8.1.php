<p><h2>8.1- Vers la Cité de Néraka</h2></p>

<p>Quête : <em>Vers la Cité de Néraka</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Dans les Monts Bacchias se situe la région de Néraka, un royaume indépendant accueillant peu de visiteurs à cause de son isolement. Cette Cité est spécialisée dans la métallurgie et est un acteur principal dans la production d'armes en Arsinoé. Ses mercenaires sont aussi très réputés dans le continent pour leur force et leur bravoure.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Même s'il vous est difficile de vous y rendre dès votre arrivée en Arsinoé, vous pouvez vous y rendre. Pour cela prenez la route du Lac Crystalmir, en parlant au Batelier choisissez la destination des Tours Violettes [ars_fild51]. Vous entrerez dès lors dans la région de Néraka.</p>

<p><center><img src="../images/acces8.1.01.jpg" alt="acces8.1.01.jpg" /></center></p>

<p>2- Vous arrivez dans la région de l'Envol de l'Aiglon, en escaladant la montagne vous arriverez près d'une demeure, celle de Sven Van Motten, loueur de gryphon. Il vous autorisera à les emprunter pour vous rendre à Néraka.</p>

<p><center><img src="../images/acces8.1.02.jpg" alt="acces8.1.02.jpg" /></center></p>

<p>3- Vous voilà chevauchant votre gryphon dans différentes régions du Royaume de Néraka. Prenez la route du Nord-Ouest pour déboucher sur la Porte de Néraka.</p>

<p><center><img src="../images/acces8.1.03.jpg" alt="acces8.1.03.jpg" /></center></p>
<p><center><img src="../images/acces8.1.04.jpg" alt="acces8.1.04.jpg" /></center></p>

<p>4- En entrant dans la porte vous serez enfin dans la Cité de Néraka.</p>

<p><center><img src="../images/acces8.1.05.jpg" alt="acces8.1.05.jpg" /></center></p>

