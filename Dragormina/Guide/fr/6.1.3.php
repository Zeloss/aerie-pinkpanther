<p><h2>6.1.3- Les Arènes de La Rana</h2></p>

<p>Quête : <em>Les Arènes de La Rana</em><br />
Pré-requis : 6.1.2- Dian Bian Fût</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Rana est une région située au Sud d'Arsinoé. Cette région est une grande forêt où vivent les La Rana. Ils étaient autrefois indépendants mais le Roi d'Arsinoé vient de les soumettre il y a de cela quelques années.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Le Batelier Erih vient de vous transporter près de La Rana. Vous allez devoir contacter la Reine de La Rana pour pouvoir négocier la liberation des otages arsinoéens. Elle souhaite la libération de son Pays pour restaurer l'Empire de La Rana.</p>

<p><center><img src="../images/acces6.1.3.01.jpg" alt="acces6.1.3.01.jpg" /></center></p>
<p><center><img src="../images/acces6.1.3.02.jpg" alt="acces6.1.3.02.jpg" /></center></p>

<p>2- Vous allez devoir apporter au Roi d'Arsinoé les conditions de la paix offerte par La Rana à Arsinoé. Le Roi avoue son impuissance ... Aucune autre légion ne peut intervenir à La Rana à cause du front du Nord avec les Gobelins. Il souhaite que vous négociez le prix de libération des otages pour que l'accord visant à l'indépendance de La Rana prenne effet.</p>

<p><center><img src="../images/acces6.1.3.03.jpg" alt="acces6.1.3.03.jpg" /></center></p>

<p>3- Retournez voir la Déesse de La Rana, elle acceptera de libérer les otages sans contre partie financière si vous réussissez à la battre dans les arènes de La Rana. </p>

<p><center><img src="../images/acces6.1.3.04.jpg" alt="acces6.1.3.04.jpg" /></center></p>
<p><center><img src="../images/acces6.1.3.05.jpg" alt="acces6.1.3.05.jpg" /></center></p>

<p>4- Vous voilà dans les arènes ! Vous devez battre la Déesse pour pouvoir remporter le titre de Libérateur de La Rana !</p>

