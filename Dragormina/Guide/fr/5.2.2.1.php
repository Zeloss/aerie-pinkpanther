<p><h2>5.2.2.1- A la recherche des larves de Kargah</h2></p>

<p>Quête : <em>A la recherche des larves de Kargah</em><br />
Pré-requis : 5.2.1.4- Le Coeur de Sidon / 9.1- La Lime du Torn-Thalas</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Il est temps de rapporter le dernier ingrédient du remède ! Ce sont les larves de Kargah, il va falloir se rendre dans cette contrée pour en prélever quelques unes.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez vous rendre dans le désert de Kargah, mais cette région est sous pression des assauts repétés des forces gobelaines de Terkish. Il vous faut avant tout s'engager dans les chevaliers d'Arsinoé pour repousser les gobelins de la lime du Torn Thalas. Il vous faut donc avoir participé à cette mission auparavant : <a href="9.1.php">La Lime du Torn-Thalas</a>. Depuis le village de Nèsos poursuivez vers l'Ouest.</p>

<p><center><img src="../images/acces9.1.09.jpg" alt="acces9.1.09.jpg" /></center></p>

<p>2- Vous arrivez dans le désert, du sable à perte de vue, aucun repère ... il va vous falloir explorer la carte pour découvrir des sorties, sachant que certaines ne mènent nulle part.</p>

<p><center><img src="../images/acces5.2.2.1.01.jpg" alt="acces5.2.2.1.01.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.1.02.jpg" alt="acces5.2.2.1.02.jpg" /></center></p>

<p>3- Selon le chemin emprunté vous arriverez à l'Oasis de Siwah ou au Temple de Pétosiris. Vous trouverez des survivants de la I Légion à Pétosiris [ars_fild49] qui résistent aux gobelins, depuis cette place vous pouvez enfin entrer dans le temple de Pétosiris.</p>

