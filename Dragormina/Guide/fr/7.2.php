<p><h2>7.2- Le Fort Orage</h2></p>

<p>Quête : <em>Le Fort Orage</em><br />
Pré-requis : 7.1- Vers l'Hylarheim</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous venez d'accéder à l'Hylarheim qui est la côté ouest de l'Arsinoé où vivent les nains hylars. Il semble que leur village soit en proie à de longs ennuis.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Le village est en cours de conversion à la vraie Foi par les missionnaires de Tyr, en lui parlant vous apprenez qu'une forteresse marine, le Fort Orage ne répond plus. De nombreux hylars gardaient cette place contre les attaques de pirates et aucun n'est revenu pour la relève. Le Frère Jude vous demande d'aller voir le Père Supérieur Malachie [ars_fild33,101,121] à Arsinoé pour qu'il puisse fournir des troupes en vue de mener un assaut sur la place.</p>

<p><center><img src="../images/acces7.2.01.jpg" alt="acces7.2.01.jpg" /></center></p>

<p>2- Le Père Malachie [ars_fild22,220,256] accepte la proposition de Frère Jude, une galère est prête à partir sous le commandement de Yadie.</p>

<p><center><img src="../images/acces7.2.02.jpg" alt="acces7.2.02.jpg" /></center></p>

<p>3- Yadie [ars_fild22,295,96] est prête à lever l'ancre avec une escouade de chevaliers d'Harpocrate et des équipements pour les Hylars. Il est temps de bouter les Da'Urdein du Fort Orage ! Elle vous proposera de vous amener à Hylarheim, de là en vous adressant au marin hylar vous pourrez être transporté jusqu'à la forteresse.</p>

<p><center><img src="../images/acces7.2.03.jpg" alt="acces7.2.03.jpg" /></center></p>

<p>4- Vous voilà au bord de la galère prêt à lancer l'instance "Fort Orage" ! Un champ de force vous empêche d'atteindre leur chef, pour cela vous allez devoir détruire dans un premier temps les Incantateurs Liche.</p>

<p><center><img src="../images/acces7.2.04.jpg" alt="acces7.2.04.jpg" /></center></p>

<p>5- Une fois le champ de force levé, vous devez affronter le chef de la garnison Da'Urdein : Koverich Orlie. Une fois la garnison éliminée, retournez sur les quais ... la mission a été accomplie !</p>

<p><center><img src="../images/acces7.2.05.jpg" alt="acces7.2.05.jpg" /></center></p>
<p><center><img src="../images/acces7.2.06.jpg" alt="acces7.2.06.jpg" /></center></p>

<p>6- Retournez voir le Frère Jude, il vous demandera de vous rendre à Da'Urdein pour obtenir des informations quant à cette attaque réalisée sans déclaration de guerre. Les Hylars qui ont fouillé la forteresse ont trouvé un pendentif permettant d'entrer dans les terres de Da'Urdein !</p>

