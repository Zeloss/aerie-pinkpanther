<p><h2>10.3- Title of Fisherman of Arsinoé</h2></p>

<p>Quest : <em>Title of Fisherman of Arsinoé</em><br />
Prerequisites : Have acces to all places in Arsinoé.</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Context</span> :<br />
Arsinoe is rich in freshwater fish and saltwater. Zeladys a fisherman Coppalana offers a fishing contest to see who is the best fisherman </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- Zeladys asks you to go buy a fishing rod in the lake Crystalmir Lyrric. This will offer you a cane to 50 pieces of silver. Accept then return to Zeladys in the beach of Coppalana. </ p>

<p><center><img src="../images/acces10.3.01.jpg" alt="acces10.3.01.jpg" /></center></p>
<p><center><img src="../images/acces10.3.02.jpg" alt="acces10.3.02.jpg" /></center></p>

<p>2- You'll have to now collect a number of fish and then show them to Zeladys to validate your quest and receive the title of Fisherman of Arsinoe.</p>
