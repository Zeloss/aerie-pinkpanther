<p><h2>7.3- To the City of Underdark</h2></p>

<p>Quest : <em>To the City of Underdark</em><br />
Pre-requisites : 7.2- Storm Fort</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Context</span> :<br />
You got a necklace for entering into the depths of Underdark. It's time to demand accountability from Da'Urdein authorities about the attack of the Fort Orage. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- From the Village of Hylarheim you must return to the Cave of Hylar [ars_fild32]. Take the south-east road to lead the Bridge Menzobaren, a bridge marking the separation between the lands controlled by Da'Urdein and zones of the Underdark. </p>

<p><center><img src="../images/acces7.3.01.jpg" alt="acces7.3.01.jpg" /></center></p>

<p>2- You follow the course of Lethe [ars_fild39] until you reach a smuggler who will offer to take you to the city of Da'Urdein. But he will not let you go, except at only one condition. He wants you to bring him the Black Pearl of Mermaid of Port-Cyrène. It's owned by Ladonnai, a siren which he was madly in love ... and this pearl was the pledge of his love. </ p>

<p><center><img src="../images/acces7.3.02.jpg" alt="acces7.3.02.jpg" /></center></p>

<p>3- Continue west to arrive in Morloc'h of Sighs [ars_fild37], continuing to the west you will eventually not find an exit and you will come to Port-Cyrène [ars_fild36]. </ p>

<p><center><img src="../images/acces7.3.03.jpg" alt="acces7.3.03.jpg" /></center></p>

<p>4- Ladonnaï lives in a cave, when she understood that you come for her pearl, she will attack you without firing a shot. After some arguments, she will give you the pearl but swear to resume it one day. </p>

<p><center><img src="../images/acces7.3.04.jpg" alt="acces7.3.04.jpg" /></center></p>

<p>5- The Passeur will be very happy to see his pearl. You even talk to other humans living in a cave in the area of Lethe. Accept that transports you to Da'Urdein and you're there!</p>

<p><em>End of the Quest : To the City of Underdark</em><br />
