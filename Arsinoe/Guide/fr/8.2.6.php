<p><h2>8.2.6- Les Mines</h2></p>

<p>Quête : <em>Les Mines</em><br />
Pré-requis : 8.2.5- La Fronde</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous êtes désormais un mercenaire de Néraka, vous allez devoir faire votre réputation en remplissant des missions diverses et variées au Hall des Mercenaires.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Pour chaque début de mission vous devez lire les annonces au panneau des missions. Votre cinquième mission est celle des Mines, vous devez vous rendre dans les anciennes mines de Néraka pour enquêter là-bas. Il semble que l'attaque de Nemours sur Néraka provienne de cette endroit.</p>

<p><center><img src="../images/acces8.2.2.01.jpg" alt="acces8.2.2.01.jpg" /></center></p>

<p>2- Près de la Porte de Néraka [ars_fild54], vous pouvez entrer dans la mine pour enquêter sur les Mages Gris. Dans la mine vous rencontrerez le Mercenaire Yavik qui vous proposera d'y pénétrer plus profondément.</p>

<p><center><img src="../images/acces8.2.6.01.jpg" alt="acces8.2.6.01.jpg" /></center></p>
<p><center><img src="../images/acces8.2.6.02.jpg" alt="acces8.2.6.02.jpg" /></center></p>

<p>3- Vous voilà dans les mines de Davaeorn et elles ne semblent pas abandonnées, elles sont le repère de l'armée des Mages Gris. Vous devez éliminer Hareishan pour pouvoir lever le premier sceau protecteur pour accéder au niveau inférieur.</p>

<p><center><img src="../images/acces8.2.6.03.jpg" alt="acces8.2.6.03.jpg" /></center></p>
<p><center><img src="../images/acces8.2.6.04.jpg" alt="acces8.2.6.04.jpg" /></center></p>

<p>4- Dans le troisième niveau de la mine, vous devez retrouver Natasha pour lever la rune protectrice.</p>

<p><center><img src="../images/acces8.2.6.05.jpg" alt="acces8.2.6.05.jpg" /></center></p>
<p><center><img src="../images/acces8.2.6.06.jpg" alt="acces8.2.6.06.jpg" /></center></p>

<p>5- Le Quatrième niveau est celui de Davaeorn, le chef des Mages Gris d'Arsinoé, fidèle soutien de Shandalar.</p>

<p><center><img src="../images/acces8.2.6.07.jpg" alt="acces8.2.6.07.jpg" /></center></p>
<p><center><img src="../images/acces8.2.6.08.jpg" alt="acces8.2.6.08.jpg" /></center></p>

