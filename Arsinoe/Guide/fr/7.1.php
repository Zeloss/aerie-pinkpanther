<p><h2>7.1- Vers l'Hylarheim</h2></p>

<p>Quête : <em>Vers l'Hylarheim</em><br />
Pré-requis : 2.4- Les faux-papiers pour Arsinoé</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Il existe un monde oublié dans les entrailles du Volcan de Gondar, l'OmbreTerre ! C'est un monde étrange et sombre où il n'y a jamais de lumière ...</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- En vous rendant dans le Volcan de Gondar, vous pouvez choisir d'entrer dans les entrailles de l'Arsinoé. Suivez la voie ferrée, puis dirigez-vous dans le Chemin des Dames [ars_fild30].</p>

<p><center><img src="../images/acces7.1.01.jpg" alt="acces7.1.01.jpg" /></center></p>
<p><center><img src="../images/acces7.1.02.jpg" alt="acces7.1.02.jpg" /></center></p>

<p>2- En poursuivant votre chemin vous arrivez face une forteresse, la Porte d'Icarie [ars_fild31] qui marque la frontière entre les terres d'Arsinoé et celles de l'OmbreTerre. Icarie, la gardienne, refuse de vous laisser passer sans autorisation des autorités d'Arsinoé.</p>

<p><center><img src="../images/acces7.1.03.jpg" alt="acces7.1.03.jpg" /></center></p>

<p>3- En parlant au fonctionnaire dans le bureau des affaires d'Arsinoé, bien qu'il soit surpris par votre demande, il l'accepte et vous donne l'autorisation. Retournez voir Icarie, elle vous laissera passer mais vous mettre en garde contre les Da'Urdein, en vous disant qu'il faut mieux les éviter mais que nous comprendrons très bien sa réticence en les voyant de nos yeux.</p>

<p><center><img src="../images/acces7.1.04.jpg" alt="acces7.1.04.jpg" /></center></p>

<p>4- En passant la porte vous vous retrouvez dans la Grotte des Hylars [ars_fild32] depuis cette grotte vous pouvez atteindre l'Hylarheim ou bien la Cité d'OmbreTerre. Pour vous rendre dans l'Hylarheim poursuivez vers l'Ouest. Vous voilà dans l'Hylarheim !</p>

<p><center><img src="../images/acces7.1.05.jpg" alt="acces7.1.05.jpg" /></center></p>

