<p><h2>2.4- Les faux-papiers pour Arsinoé</h2></p>

<p>Quête : <em>En route vers Arsinoé, la Cité</em><br />
Pré-requis : 2.3- Le Réveil des Morts de Kiman Farès</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
L'accès à Arsinoé nécessite une autorisation des autorités de la ville. Hélas les autorités n'en délivrent plus. Le chef Pevnec vous a indiqué un contact qui pourra vous en fournir des faux.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Le Chef Pevnec vous a indiqué son Contact à Euhéméria, Philerme. Retournez dans la forêt de Kiman Farès [ars_fild01] puis prenez la route du nord-ouest. Vous voici dans la Cité de la Magie, Euhéméria [ars_fild05] L'auberge se situe en [ars_fild05,206,193], à l'intérieur vous trouverez Philerme, donnez lui la lettre de Pevnec [ars_in01,220,57].</p>

<p><center><img src="../images/acces2.4.01.jpg" alt="acces2.4.01.jpg" /><br />
<img src="../images/acces2.4.02.jpg" alt="acces2.4.02.jpg" /></center></p>

<p>2- Il vous faut maintenant les matériaux pour fabriquer les faux-papiers. Il vous faut de l'encre et du papier que pourra vous fournir Raichor dans la cité portuaire de Thébaïde. Retournez à Soknobeus [ars_fild03] puis prenez la route menant vers le Sud-Est. Vous voilà dans la Jungle de Thébaïde [ars_fild17], continuez vers l'est et vous arriverez dans la région de la ville de Thébaïde [ars_fild18]. Raichor se situe près du port. [ars_fild18,153,49]</p>

<p><center><img src="../images/acces2.4.03.jpg" alt="acces2.4.03.jpg" /></center></p>

<p>3- Là ça se corse ... Il vous faut atteindre la ferme eupator [ars_fild19] mais le tunnel étant bouché vous allez devoir contourner la montagne en passant par les Monts Bacchas. Retournez à Euhéméria puis prenez la direction du Nord-Est. Vous voilà à l'Abbaye du Feymour [ars_fild07], continuez vers l'est pour atteindre la Citadelle de la Lumière [ars_fild16]. Là-bas vous trouverez un batelier [ars_fild16,152,144] qui vous permettra de traverser le Lac Crystalmir. Crystalmir est un petit village de pêcheur dans les Monts Bacchias [ars_fild20], prenez le chemin à l'est. Vous voilà enfin arrivé à la Ferme Eupator ! Rick Eupator [ars_fild19,137,128] se proposera de raffiner de l'encre si vous lui apportez 140 unités d'encre des poulpes des forêts.</p>

<p><center><img src="../images/acces2.4.04.jpg" alt="acces2.4.04.jpg" /><br />
<img src="../images/acces2.4.05.jpg" alt="acces2.4.05.jpg" /></center></p>

<p>4- Les poulpes sont autour de la ferme. Une fois les 140 unités d'encre données à Rick Eupator vous recevrez une Encre Vermillon Purifiée. Apportez l'encre à Raichor [ars_fild18,153,49]. Il vous demandera de lui fournir 400 pièces de la monnaie d'Arsinoé pour finaliser la transaction. Maintenant il faut retourner voir Philerme à Euhéméria.</p>

<p><center><img src="../images/acces2.4.06.jpg" alt="acces2.4.06.jpg" /></center></p>

<p>5- Philerme connait un passage pour éviter les contrôles des autorités car vous n'avez pas encore le tampon officiel. Vous devez vous rendre à l'entrée de la mine d'Euhéméria. [ars_fild05,23,155]</p>

<p><center><img src="../images/acces2.4.07.jpg" alt="acces2.4.07.jpg" /></center></p>

<p>6- Vous voilà enfin dans la cité d'Arsinoé ! Il va falloir trouver le fonctionnaire Georet. Il se situe dans les bâtiments administratifs au sommet d'Arsinoé [ars_fild22,188,202]. Parlez-lui et voilà vous avez vos papiers en règle !</p>

