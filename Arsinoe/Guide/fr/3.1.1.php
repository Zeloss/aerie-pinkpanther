<p><h2>3.1.1-Les Grottes des Triplés</h2></p>

<p>Quête : <em>Les Grottes des Triplés</em><br />
Pré-requis : 11.2.1- Les Bandits d'Euhéméria</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Culte de Danifae est très secret et peu de personnes le connaisse. Il est basé sur l'adoration des enfants de cette déesse.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez rencontrer Julien l'Apostat au temple oecuménique d'Euhéméria.[ars_in01,75,142] Selon lui les triplés de Danifae ne répondent plus aux prières des fidèles et sont même devenus agressifs ! Vous allez devoir trouver les trois grottes secrètes et enquêter dessus.</p>

<p><center><img src="../images/acces3.1.1.01.jpg" alt="acces3.1.1.01.jpg" /></center></p>

<p>2- Vous trouverez un prêtre de Danifae au Col du Faymour [ars_fild07,146,99]il vous parlera du premier temple qui se situe à quelques mètres. Vous trouverez à l'intérieur de cette grotte Calliope que vous devez tuer. Une fois fait prenez le portail vous receverez l'Âme de Calliope. Retournez discuter avec le Prêtre de Danifae, il vous dit que vous devez découvrir la localisation des autres temples.</p>

<p><center><img src="../images/acces3.1.1.02.jpg" alt="acces3.1.1.02.jpg" /></br>
<img src="../images/acces3.1.1.03.jpg" alt="acces3.1.1.03.jpg" /></center></p>

<p>3- Retournez voir Julien l'Apostat, il vous expliquera la situation au vu des éléments découverts dans la grotte. Le deuxième temple se situe à Coppalana [coppa,286,292] et cette fois-ci il va vous falloir récupérer l'âme d'Euterpe.</p>

<p><center><img src="../images/acces3.1.1.04.jpg" alt="acces3.1.1.04.jpg" /></center></p>

<p>4- La troisième grotte se situe dans la bibliothèque d'Euhéméria. Une fois à l'intérieur, il vous faudra éliminer Uranie pour récupérer son âme.</p>

<p><center><img src="../images/acces3.1.1.05.jpg" alt="acces3.1.1.05.jpg" /></center></p>

<p>5- Maintenant que vous avez les trois âmes, vous pouvez les rapporter à Julien l'Apostat. Vous serez récompensé, il vous conseillera de rendre visite au temple principal de Danifae dans la capitale, Arsinoé.</p>

