<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Il vous faut du sang de Shogunka pour préparer le remède. Des infirmiers sont déjà partis mais ne sont toujours pas revenus. Vous devez vous rendre au Lac aux Fées. Vous vous êtes déjà rendus dans cette région lorsque vous avez rencontré Fernandel et sa vache à Urunesti. Cette fois-ci descendez pour rencontrer l'infirmier Carly.</p>

