<p><h2>5.2.2.3- Les Entrailles du Temple</h2></p>

<p>Quête : <em>Les Entrailles du Temple</em><br />
Pré-requis : 5.2.2.2 Le Corps de Djoser</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Pour pouvoir accèder aux larves de Kargah ainsi qu'au Portail des Abysses, vous allez devoir lever les runes protectrices du Temple de Pétosiris.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- La première porte passée vous arrivez dans une série de couloirs avec des portes à débloquer et ce en apportant différents objets de l'Arsinoé. Vous devez ouvrir une série de quatre portes dans l'ordre, dès que vous voudrez repasser une porte il vous faudra activer le sort à nouveau en y déposant les objets.</p>

<p><center><img src="../images/acces5.2.2.3.01.jpg" alt="acces5.2.2.3.01.jpg" /></center></p>

<p>2- Après les portes qui vous auront retenus un certain temps. Vous arrivez dans une petite salle ... et vous voilà face à Ursula Hapoi ! Qui se demande bien comment se fait-il qu'elle ne vous a pas vu emprunter son tunnel ... et pour cause vous venez de lever toutes les protections alors que vous auriez pu contourner le tout ! Il est temps d'affronter les larves ... Votre mission est prioritaire !</p>

<p><center><img src="../images/acces5.2.2.3.02.jpg" alt="acces5.2.2.3.02.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.3.03.jpg" alt="acces5.2.2.3.03.jpg" /></center></p>

<p>3- Vous voilà face à la grande larve de Kargah dans un atrium. Cet insecte doit être écrasé pour pouvoir poursuivre. A la sortie vous vous retrouverez à l'entrée Ouest du Temple avec un chemin menant au Portail des Abysses.</p>

<p><center><img src="../images/acces5.2.2.3.04.jpg" alt="acces5.2.2.3.04.jpg" /></center></p>
<p><center><img src="../images/acces5.2.2.3.05.jpg" alt="acces5.2.2.3.05.jpg" /></center></p>

<p>4- Tous les ingrédients pour le remède sont en votre possession, il est temps de sauver de la population d'Urunesti en retournant à l'Hôpital du Bras Secoureur pour retrouver Dumont d'Urville. Vous obtiendrez le Titre de Médecin de l'Urunesti !</p>

