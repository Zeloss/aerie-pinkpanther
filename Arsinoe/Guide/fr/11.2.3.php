<p><h2>11.2.3- Les Contrebandiers</h2></p>

<p>Quête : <em>Les Contrebandiers</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Cité d'Euhéméria est une Cité de la Magie. Le Bourgmestre d'Euhéméria propose aux aventuriers des quêtes.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez rencontrer à Euhéméria le Bourgmestre [ars_fild05,195,193]. Il vous proposera différentes quêtes, choisissez celle des Contrebandiers. Il vous parlera d'un petit port au sud de la forêt de Kiman Farès servant de lieu au transit de marchandises illégales. Les Chevaliers d'Euhéméria ne pouvant intervenir aussi loin, il vous demande de mettre fin à ses pratiques douteuses.</p>

<p><center><img src="../images/acces11.2.1.01.jpg" alt="acces11.2.1.01.jpg" /></center></p>

<p>2- Il va falloir vous rendre à Soknobeus [ars_fild03] puis prendre la route du sud-ouest pour atteindre la région du Phare de Traïana [ars_fild04]. Vous devez trouver sur place Jorbert Surcouf [ars_fild04,93,113], étant sur les nerfs, il ne mettra pas longtemps avant de vous attaquer avec ses sbires. Après une petite bousculade avec lui, il reconnaitra sa défaite. Retournez voir le bourgmestre d'Euhéméria pour obtenir votre récompense.</p>

<p><center><img src="../images/acces11.2.3.01.jpg" alt="acces11.2.3.01.jpg" /></center></p>

