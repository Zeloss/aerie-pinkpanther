<p><h2>6.1.1- Les Colons de La Rana</h2></p>

<p>Quête : <em>Les Colons de La Rana</em><br />
Pré-requis : 2.4- Les faux-papiers pour Arsinoé</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Rana est une région située au Sud d'Arsinoé. Cette région est une grande forêt où vivent les La Rana. Ils étaient autrefois indépendants mais le Roi d'Arsinoé vient de les soumettre il y a de cela quelques années.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez vous rendre à la Porte Sud d'Arsinoé pour aboutir dans l'Empire de Rana : Les Deux-Portes [ars_fild25], ceci est l'entrée vers les forêts de La Rana. Le Général Lorill est dépité, il ne peut pas rejoindre son armée, la quatrième Légion, il craint qu'une révolte des indigènes soit en cours. Le Générak Lorill vous demande de rejoindre le Maréchal Akey de la Porte d'Istar pour vous que vous lui remettiez son rapport.</p>

<p><center><img src="../images/acces6.1.1.01.jpg" alt="acces6.1.1.01.jpg" /></center></p>

<p>2- Le soulèvement de La Rana est en cours. Le Maréchal Akey souhaite que vous escortiez le Général Lorill vers les retranchements de la IV Légion pour les sauver. Retournez voir Lorill, il vous proposera de passer en force les Hauts Plateaux de la Rana pour rejoindre au plus vite ses soldats. Prenez la route du sud pour changer de région, vous apercevrez dès lors le Soldat Démérol qui vous proposera de lancer l'instance "Hauts-Plateaux de La Rana".</p>

<p><center><img src="../images/acces6.1.1.02.jpg" alt="acces6.1.1.02.jpg" /></center></p>
<p><center><img src="../images/acces6.1.1.03.jpg" alt="acces6.1.1.03.jpg" /></center></p>

<p>3- Vous devez traverser la région en vous frayant un chemin parmi les créatures de La Rana. Pour pouvoir passer ce pont vous devez avoir sécurisé la zone précédente.</p>

<p><center><img src="../images/acces6.1.1.04.jpg" alt="acces6.1.1.04.jpg" /></center></p>
<p><center><img src="../images/acces6.1.1.05.jpg" alt="acces6.1.1.05.jpg" /></center></p>

<p>4- Vous devez ensuite vous rendre au temple, les éclaireurs y ont rapporté du grabuge. Il vous faudra y tuer un Héros de La Rana qui défend cette contrée. </p>

<p><center><img src="../images/acces6.1.1.06.jpg" alt="acces6.1.1.06.jpg" /></center></p>
<p><center><img src="../images/acces6.1.1.07.jpg" alt="acces6.1.1.07.jpg" /></center></p>

<p>5- Vous arrivez dans une colonie d'Arsinoé ... aucun survivant ... Le Général Lorill vous demande de prendre le bateau pour descendre le fleuve. Vous y trouvez des survivants un peu plus loin, ainsi que le Batelier Erih.</p>

