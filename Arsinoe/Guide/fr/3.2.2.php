<p><h2>3.2.2- Le Monastère d'Harpocrate</h2></p>

<p>Quête : <em>Le Monastère d'Harpocrate</em><br />
Pré-requis : 3.2.1- La Tour des Etoiles / 2.4- Les faux-papiers pour Arsinoé</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Bouclier d'Harpocrate, artefact très puissant a été volé. Après avoir été orienté sur une fausse piste avec le Nachtstern et la tour des Etoiles, le Clerc Graivois oriente désormais son enquête vers l'Académie d'Harpocrate dans la cité d'Arsinoé.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez vous rendre à l'Académie d'Harpocrate à Arsinoé. [ars_fild22,290,162] Dans le hall d'entrée Ribald vous attend, il vous donnera de précieux conseils pour enquêter sans être inquiété par les gardes de l'Académie.</p>

<p><center><img src="../images/acces3.2.2.01.jpg" alt="acces3.2.2.01.jpg" /></center></p>
<p><center><img src="../images/acces3.2.2.02.jpg" alt="acces3.2.2.02.jpg" /></center></p>

<p>2- Dans la salle d'audience vous trouverez l'Abbé Rosalite ainsi que son conseiller Tonsuret. Ils seront déçus que vous n'avez pas pu trouver le Heaume d'Harpocrate ... Tonsuret vous accusera de complicité avec le NachtStern ! Il semble que vous ne soyez pas les bienvenus.</p>

<p><center><img src="../images/acces3.2.2.03.jpg" alt="acces3.2.2.03.jpg" /></center></p>

<p>3- Dans les dortoirs vous trouverez d'Arimathie [ars_in04,24,152] qui vous confirmera que des choses étranges se déroule dans l'Académie. Il vous conseille de fouiller la prison, mais pour cela vous devez trouver où Tonsuret cache les clés.</p>

<p><center><img src="../images/acces3.2.2.04.jpg" alt="acces3.2.2.04.jpg" /></center></p>

<p>4- Les clés de la prison se situent dans un bureau [ars_in04,157,69], partez ensuite vers l'entrée de la prison depuis les dortoirs. Et surprise, vous trouvez de nombreux moines dans la prison qui vous parlent de Doppelganger ... des monstres qui prennent l'apparence de leur victime.</p>

<p><center><img src="../images/acces3.2.2.05.jpg" alt="acces3.2.2.05.jpg" /></center></p>

<p>5- Vous y trouverez le vrai Tonsuret ! Parlez-lui et il vous proposera de combattre les Doppelgangers. L'Instance de l'Assaut sur l'Académie débutera vous devez vous débarasser des doppelgangers qui ont pris l'apparence des moines. Lorsque vous tuez Ismail le Fourbe, Tonsuret vous ouvrira un Portail dans la salle du Trône. </p>

<p><center><img src="../images/acces3.2.2.06.jpg" alt="acces3.2.2.06.jpg" /></center></p>

<p>6- En bon soldat vous revenez faire votre rapport au Clerc Graivois [ars_fild06], il sera très étonné par la présence de Doppelganger qui coûtent très cher à recruter donc quelqu'un de très puissant et riche a mené cette attaque. Quoiqu'il en soit, il vous manque toujours le Heaume d'Harpocrate qui n'a pas été retrouvé.</p>

