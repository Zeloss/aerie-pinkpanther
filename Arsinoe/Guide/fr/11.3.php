<p><h2>11.3- La Plante du désert</h2></p>

<p>Quête : <em>La Plante du désert</em><br />
Pré-requis : 9.1- La Lime du Torn-Thalas</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Une maladie étrange ravage le village de Kiman Farès. Le Docteur du village a besoin d'aide, il manque de vivres comme de médicaments.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Prigent Danon vous demandera de rendre visite à l'Herboriste d'Euhéméria pour rapporter quelque herbes pouvant servir de remède.</p>

<p><center><img src="../images/acces11.3.01.jpg" alt="acces11.3.01.jpg" /></center></p>

<p>2- L'herboriste a bien les herbes, vous vous chargez de transporter la marchandise. Une fois de retour à Kiman Farès, Prigent Danon vous explique qu'il lui manque une plante très rare pour préparer le remède. Il a besoin d'une Lophophora qui ne pousse que dans le désert de Kargah.</p>

<p><center><img src="../images/acces11.3.02.jpg" alt="acces11.3.02.jpg" /></center></p>

<p>3- La plante se trouve bel et bien dans le désert de Kargah, il ne vous manque plus qu'à la ramasser ... mais elle ne se laissera pas faire !</p>

<p><center><img src="../images/acces11.3.03.jpg" alt="acces11.3.03.jpg" /></center></p>

<p>4- Retournez voir Danon Prigent pour finir votre quête et sauver le fils du chef du village de ses terribles blessures infligées par un Turlutte.</p>

