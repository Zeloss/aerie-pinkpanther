<p><h2>3.2.1- La Tour des Etoiles</h2></p>

<p>Quête : <em>La Tour des Etoiles</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Bouclier d'Harpocrate, un artefact très puissant a été volé. Vous devez enquêter sur cette mystérieuse disparition. Harpocrate est la divinité des chevaliers d'Arsinoé, en accomplissant cette mission vous pourrez devenir un membre de l'armée d'Arsinoé.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Pour débuter la quête vous devez rencontrer le Clerc Graivois [ars_fild06,56,50]. Il se situe à la Porte d'Istar [ars_fild06] qui est la porte est d'Arsinoé, la Cité. Pour vous y rendre vous devez prendre la route ouest depuis Euhéméria. La situation militaire à Arsinoé est critique de partout des forces maléfiques agissent. Vous devez apporter à Ribald une lettre lui donnant des indications sur sa mission à laquelle vous serez convié.</p></p>

<p><center><img src="../images/acces3.2.1.01.jpg" alt="acces3.2.1.01.jpg" /></center></p>

<p>2- Ribald se situe donc dans la bibliothèque d'Euhéméria.[ars_in01,150,143] Vous êtes officielement un écuyer des chevaliers d'Arsinoé. Bien que contrôlant une grande partie du continent, les chevaliers d'Arsinoé ne contrôlent toujours pas la Tour des Etoiles qui est au main du NachtStern. Vous avez rendez-vous avec Ribald et quelques uns de ses hommes, vous devez prendre la route sud-est depuis le village de Soknobeus [ars_fild03].</p>

<p><center><img src="../images/acces3.2.1.02.jpg" alt="acces3.2.1.02.jpg" /></center></p>

<p>3- Vous retrouverez Ribald au pied de la tour. Vous allez devoir ouvrir un chemin et gravir les étages. Pour monter à l'étage suivant vous devez tuer tous les monstres présents. 5 étages vous attendent avant d'atteindre le sommet de la tour. </p>

<p><center><img src="../images/acces3.2.1.03.jpg" alt="acces3.2.1.03.jpg" /></center></p>

<p>4- Vous devez tuer le Seigneur de Tour le NachtStern. Une cinématique débutera vous expliquant la non-responsabilité de celui-ci dans la disparition du Boulcier d'Harpocrate. En tant qu'ancien chevalier lui-même, il conseille à Ribald d'enquêter au Monastère d'Harpocrate où selon lui le mal réside. A la fin de ces explications prenez le portail pour recevoir une récompense.</p>

<p><center><img src="../images/acces3.2.1.04.jpg" alt="acces3.2.1.04.jpg" /></center></p>

<p>5- Retournez voir le Clerc Graivois, il a reçu le rapport de Ribald. Il avait lui aussi des doutes sur la hiérarchie de l'Académie d'Harpocrate dans la cité d'Arsinoé. Il vous donnera une lettre permettant d'être reçu à l'Académie pour pouvoir enquêter officieusement avec Ribald qui est lui aussi en route vers l'Académie.</p>

