<p><h2>9.2- L'Oasis de Siwah</h2></p>

<p>Quête : <em>L'Oasis de Siwah</em><br />
Pré-requis : 9.1- La Lime du Torn-Thalas</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Nord d'Arsinoé est un vaste désert, le désert de Kargah. Depuis des siècles des colons de l'Arsinoé ont pris les terres des gobelins, les repoussant jusque dans les régions les plus désolées. Malheureusement, ils ont mené une grande attaque sur Arsinoé, vous venez de les repousser du Fleuve Torn-Thalas, vous allez devoir poursuivre le combat vers l'Oasis de Siwah. </p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Votre victoire sur la Lime du Torn-Thalas a redonné du courage aux soldats de la III Légion d'Arsinoé. Le Village de Nèsos a été repris aux ennemis. Le Sénéchal Lin s'y trouve, il vous donnera l'ordre de marche vers l'Oasis de Siwah. </p>

<p><center><img src="../images/acces9.2.01.jpg" alt="acces9.2.01.jpg" /></center></p>

<p>2- Vous devez maintenant traverser une partie désolée du désert, sans aucun point d'eau. Vous devez emprunter le bon chemin, au risque de vous retrouver au Temple de Pétosiris.</p>

<p><center><img src="../images/acces9.2.02.jpg" alt="acces9.2.02.jpg" /></center></p>

<p>3- Vous retrouverez le Sénéchal Lin dans un fort près de l'Oasis de Siwah, les forces se sont rassemblées et prêtes pour l'attaque. Vous devez entrer dans l'Oasis de Siwah pour sauver les soldats de la II Légion qui y sont bloqués depuis des semaines.</p>

<p><center><img src="../images/acces9.2.03.jpg" alt="acces9.2.03.jpg" /></center></p>

<p>4- Premièrement vous devez libérer la ferme pour pouvoir y placer un poste de commandement, vous devez éliminer toutes les forces goblines.</p>

<p><center><img src="../images/acces9.2.04.jpg" alt="acces9.2.04.jpg" /></center></p>

<p>5- Une garnison gobline se situe entre vous et l'Oasis, vous devez prendre la place avant de continuer. Une fois fait empruntez le pont.</p>

<p><center><img src="../images/acces9.2.05.jpg" alt="acces9.2.05.jpg" /></center></p>

<p>6- Et vous y voilà, l'Oasis de Siwah est une grande caverne avec une source naturelle en eau. Le Ravitaillement va désormais pouvoir se faire, ainsi que l'union entre la II et la III Légion.</p>

<p><center><img src="../images/acces9.2.06.jpg" alt="acces9.2.06.jpg" /></center></p>

<p>7- Vous voilà enfin dans l'Oasis de Siwah, la situation est assez critique, les gobelins attaquent de façon quasi continue pour protéger leur Citadelle de Terkish qui n'est plus bien loin. Contactez le Colonel de Nursie pour lancer la défense de l'Oasis.</p>

<p><center><img src="../images/acces9.2.07.jpg" alt="acces9.2.07.jpg" /></center></p>

<p>8- Vous allez devoir repousser 8 vagues d'assaut de Gobelins, vous devez tenir plus de 30 minutes.</p>

<p><center><img src="../images/acces9.2.08.jpg" alt="acces9.2.08.jpg" /></center></p>

