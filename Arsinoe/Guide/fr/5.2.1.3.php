<p><center><img src="../images/acces5.2.1.3.02.jpg" alt="acces5.2.1.3.02.jpg" /></center></p>

<p>3- Vous voilà dans l'instance "Temple du Dagon", vous devez éliminer le Festin de Pierre qui bloque l'entrée. Une fois le gardien du Temple de Dagon hors d'état de nuire, vous pourrez rentrer dans le Temple Principale, porte menant à Sidon.</p>

