<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous devez récupérer une algue curative dans la région de Sidon. Sidon est une région sous-marine de l'Arisoné. Cette fois-ci vous serez accompagné d'Ursula Hapoi une exploratrice de renom qui a pour objectif d'aider elle aussi les habitants d'Urunesti.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Ursula Hapoi prépare la descente non loin de l'Hopital du Bras Secoureur.  Elle donnera le matériel pour descendre, activez la machine pour descendre dans les abîmes.</p>

<p><center><img src="../images/acces5.2.1.3.01.jpg" alt="acces5.2.1.3.01.jpg" /></center></p>

