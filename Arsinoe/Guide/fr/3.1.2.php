<p><h2>3.1.2-A la recherche du Prophète Tzangue</h2></p>

<p>Quête : <em>A la recherche du Prophète Tzangue</em><br />
Pré-requis : 3.1.1-Les Grottes des Triplés / 2.4- Les faux-papiers pour Arsinoé</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
La Déesse Danifae ne répond plus aux prières, les grottes secrètes au culte de ses triplés sont envahies par des monstres et vous venez de sauver leur âme. Cela commence à devenir étrange ...</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Julien l'Apostat vous offre la possibilité de visiter le temple principal dédié à la Déesse Danifae. Une fois l'autorisation d'accéder à Arsinoé en poche, rendez-vous devant le temple. [ars_fild22,240,299] Le Prélat Anthiole vous parlera d'une autre disparition inquiétante, celle du Prophète Tzangue qui possède un lien très particulier au divin. Selon le prélat, celui-ci aurait été aperçu par la Mage Gawëlle dans les Monts Bacchias.</p>

<p><center><img src="../images/acces3.1.2.01.jpg" alt="acces3.1.2.01.jpg" /></center></p>

<p>2- Retournez voir le Batelier de la Citadelle de la Lumière. Il vous proposera une traversée vers le temple de la solitude mais pour 600 Pièces d'Argent ! Vous n'êtes hélas pas en mesure de refuser l'offre.</p>

<p><center><img src="../images/acces3.1.2.02.jpg" alt="acces3.1.2.02.jpg" /></center></p>
<p><center><img src="../images/acces3.1.2.03.jpg" alt="acces3.1.2.03.jpg" /></center></p>

<p>3- Vous y voilà ! Et quelle chance, vous croisez le Prophète Tzangue à l'entrée du Temple. Lorsque vous lui apprenez que vous avez les âmes des triplés, il se met en joie et vous propose d'entrer avec lui pour annoncer la bonne nouvelle à la Déesse Danifae. Hélas une fois à l'intérieur celui-ci commence à se sentir mal, à ressentir des forces maléfiques. En effet il va falloir vous battre une fois de plus pour éliminer le démon qui occupe le Temple sacré.</p>

<p><center><img src="../images/acces3.1.2.04.jpg" alt="acces3.1.2.04.jpg" /></center></p>

<p>4- Tzangue et la Déessse Danifae apparaissent à la mort du monstre près de l'autel du Temple. Danifae nous explique pourquoi elle ne pouvait plus répondre aux prières de ses fidèles car ses enfants avaient été volés par Gayzor, le démon que vous venez d'éliminer. Un portail apparaitra, engouffrez-vous dedans, vous serez téléporté dans le village de Crystalmir.</p>

<p><center><img src="../images/acces3.1.2.05.jpg" alt="acces3.1.2.05.jpg" /></center></p>

