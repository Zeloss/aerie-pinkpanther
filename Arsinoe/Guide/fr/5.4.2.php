<p><h2>5.4.2- La Descente aux enfers</h2></p>

<p>Quête : <em>La Descente aux enfers</em><br />
Pré-requis : 5.4.1- Le Portail des Abysses</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous y voilà, les Abysses, un monde terne emplit de monstres sauvages. Il va falloir vous y enfoncer avec l'aide des groupes armés d'Arsinoé.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Dans la première région, vous ne pouvez avancer très loin car un champ de force bleu vous bloque l'accès. Pour lever la barrière vous devez tuer Warika et Chikai, ensuite il vous faudra éliminer les monstres du tableau pour pouvoir passer au suivant.</p>

<p><center><img src="../images/acces5.4.2.01.jpg" alt="acces5.4.2.01.jpg" /></center></p>
<p><center><img src="../images/acces5.4.2.02.jpg" alt="acces5.4.2.02.jpg" /></center></p>

<p>2- En avançant vous rencontrerez Ribald et ses moine qui combattent eux aussi dans les Abysses, dans ce tableau vous allez devoir éliminer les démons pour ouvrir le deuxième portail, ensuite un Démon Supérieur Atila qu'il vous faudra élimliner pour passer au prochain tableau.</p>

<p><center><img src="../images/acces5.4.2.03.jpg" alt="acces5.4.2.03.jpg" /></center></p>
<p><center><img src="../images/acces5.4.2.04.jpg" alt="acces5.4.2.04.jpg" /></center></p>

<p>3- Dans ce troisième tableau vous allez rencontrer les troupes de la III Légion avec le Sénéchal Lin et le Colonel de Nursie. Vous devez éliminer les abominations pour atteindre le haut des escaliers. Vous devez éliminer Sloth mias une fois cela fait, le tableau recommence une deuxième fois ! Recommencez la même opération ... Hélas l'Instance recommence une troisième fois ... Lors de la troisième tentative, l'Archimage Ornélia et Dynaheir viennent vous aider à sortir de cette boucle temporelle de laquelle vous n'arrivez pas à vous en sortir. Un portail sera ouvert et vous pourrez accéder au quatrième tableau.</p>

<p><center><img src="../images/acces5.4.2.05.jpg" alt="acces5.4.2.05.jpg" /></center></p>
<p><center><img src="../images/acces5.4.2.06.jpg" alt="acces5.4.2.06.jpg" /></center></p>

<p>4- Dans ce dernier tableau vous serez en présence des triplés de Danifae. Vous devez simplement traverser la région sans tomber sous les coups des démons. En poursuivant votre route vous déboucherez à l'entrée de la ville d'Ust Nashar, le Coeur des Abysses, Royaume de Fenrir.</p>

