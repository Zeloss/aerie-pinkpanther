<p><h2>2.3- Le Réveil des Morts de Kiman Farès</h2></p>

<p>Quête : <em>La Cité de Gomorrhe</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Pont Daggard étant reconstruit vous pouvez désormais vous aventurer plus profondément dans les terres d'Arsinoé, dans la forêt de Kiman Farès. Cette forêt abrite plusieurs villages et permet d'accéder à Euhéméria et à la Jungle de la Thébaïde.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Le Chef Rafloc'h souhaite que vous apportiez une lettre à son homologue le Chef Pevnec. Vous devez traverser le Forêt de Kiman Farès [ars_fild01] puis vous diriger vers l'est. Vous arrivez dans une nouvelle partie de la forêt, partie où se situe le village même de Kiman Farès. Vous pouvez dès lors rencontrer le Chef Pevnec et lui donner votre lettre. [ars_fild02,249,212]</p>

<p><center><img src="../images/acces2.3.01.jpg" alt="acces2.3.01.jpg" /><br />
<img src="../images/acces2.3.02.jpg" alt="acces2.3.02.jpg" /></center></p>

<p>2- Le Chef Pevnec vous apprendra que son village subit l'attaque de Morts-Vivants depuis quelques temps. Il recherche des aventuriers pour les aider à mettre fin à ce cauchemar. Il vous conseille de rendre visite au Shaman Kerloic. Celui-ci se trouve à l'ouest dans une maison abandonnée. [ars_fild02,31,199] Il vous parlera d'une ancienne cité, Gomorrhe qui fut détruire par la déesse du Mont Bacchias, il y a de cela des siècles. Il vous recommande de commencer l'enquête dans cette nécropole.</p>

<p><center><img src="../images/acces2.3.03.jpg" alt="acces2.3.03.jpg" /></center></p>

<p>3- Retournez sur vos pas dans la forêt de Kiman Farès [ars_fild01] L'entrée de la nécropole se situe à l'ouest. Une fois à l'intérieur avancez, mais vous rencontrez deux statues gardiennes qui vous refusent l'entrée. En leur parlant vous apprenez que seul les membres de la Famille Royale de Gomorrhe peuvent entrer dans la Nécropole. Retournez voir le Shaman Kerloic. Il vous conseille d'enquêter au village de Soknobeus où parait-il se situerait les descendants des Gommorhiens.</p>

<p><center><img src="../images/acces2.3.04.jpg" alt="acces2.3.04.jpg" /></center></p>

<p>4- Dirigez-vous vers le sud de Kiman Farès. Continuez vers le sud pour arriver dans le village de Soknobeus pour rencontrer Ataturke. [ars_fild03,213,46] Il vous parlera d'un petit cimetière où reposent les survivants de Gomorrhe. Vous y trouverez un symbole de la Famille Royale. Retournez voir Shaman Kerloic pour qu'il puisse vous donner sa bénédiction avant d'entrer dans la nécropole.</p>

<p><center><img src="../images/acces2.3.05.jpg" alt="acces2.3.05.jpg" /><br />
<img src="../images/acces2.3.06.jpg" alt="acces2.3.06.jpg" /></center></p>

<p>5- L'heure du combat a sonné ! Vous allez vite vous rendre compte que le Roi de Gomorrhe ne prend aucun coup ... Il va vous falloir tuer auparavant les quatre Mage-Liche pour affaiblir le Roi. Ensuite attaquer efficacement le Roi Liche pour le terrasser. Une fois mort vous devez parler à Malemort, un aventurier qui vous donnera quelques objets qu'il a récupéré dans la crypte.</p>

<p><center><img src="../images/acces2.3.08.jpg" alt="acces2.3.08.jpg" /><br />
<img src="../images/acces2.3.07.jpg" alt="acces2.3.07.jpg" /></center></p>

<p>6- Une fois le Roi mis hors d'état de nuire, retournez voir le Shaman Kerloic, selon lui la situation n'a changé en rien ... La cause devait être autre. Le Chef Pevnec va continuer les recherches, mais en lui parlant vous apprenez qu'aucun voyageur n'est autorisé à entrer dans la cité d'Arsinoé depuis la Porte d'Istar. Pour l'aide que vous lui avez apporté, il se propose de vous aider à faire des faux-papiers. Vous devez rencontrer son contact, Philerme, dans une auberge d'Euhéméria.</p>

