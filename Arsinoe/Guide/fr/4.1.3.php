<p><h2>4.1.3- Réunion des sages</h2></p>

<p>Quête : <em>Réunion des sages</em><br />
Pré-requis : 3.4- Agathon Paraclet</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
L'Archimage Ornélia concentre désormais le front de résistance face aux ténèbres qui s'agitent en Arsinoé. La menace se précise, les Mages Gris sont derrière tous les malheurs de l'Arsinoé. La lutte contre Shandalr la liche s'organise autour de l'Archimage Ornélia.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Revenez voir Ornélia une fois que vous aurez reçu votre titre d'Inquisiteur. Elle vous invitera à la réunion des sages, une réunion entre l'Ordre Magique et les Ordres Religieux et Militaires.</p>

<p><center><img src="../images/acces4.1.3.01.jpg" alt="acces4.1.3.01.jpg" /></center></p>

<p>2- Durant les débats, coup de théâtre ! Shandalar la liche vient narguer les participants en leur dévoilant ses plans. Il compte ouvrir un portail vers les abysses pour déclencher le Ragnarök ! C'était pour cela qu'il a volé les artefacts les plus puissants d'Arsinoé pour pouvoir lancer ce sort très puissant avec l'aide des Mages Gris. Vous devrez vous débarasser des deux seigneurs de guerre qu'il vous a laissé.</p>

<p><center><img src="../images/acces4.1.3.02.jpg" alt="acces4.1.3.02.jpg" /></center></p>

<p>3- En sortant de la salle de réunion, elle vous proposera de réunir vos forces pour mener l'assaut final sur la Citadelle de Lumière, lieu où Shandalar compte lancer son sort. Vous devez l'en empêcher à tout prix !</p>

